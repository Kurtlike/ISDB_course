package com.jokend.db.db.pojoDBClasses;


public class Families {

  private long id;
  private long inn;
  private String role;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public long getInn() {
    return inn;
  }

  public void setInn(long inn) {
    this.inn = inn;
  }


  public String getRole() {
    return role;
  }

  public void setRole(String role) {
    this.role = role;
  }

}
